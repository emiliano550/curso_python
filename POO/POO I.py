#¿Cual es la naturaleza de un Objeto en la vida real?
# Los objetos tienen un ESTADO, un COMPORTAMIENTO (¿Qué puede hacer?), y unas PROPIEDADES.

#Pongamos un ejemplo: El objeto coche.
#¿Cuál es el ESTADO de un coche? :Un coche puede estar parado, circulando, estacionado etc
# ¿Qué PROPIEDADES tiene un coche?: Un coche tiene un color, un peso, un tamaño etc
# ¿Qué COMPORTAMIENTO tiene un coche?: Un coche puede arrancar, frenar, acelerar, frenar, girar etc.

#OBJETO: 
# Tiene propiedades (ATRIBUTOS)
# Color, peso, altura, largo etc...

#Tiene un comportamiento (¿Qué es capaz de hacer?):
# Arrancar, frenar, girar, acelerar etc..

